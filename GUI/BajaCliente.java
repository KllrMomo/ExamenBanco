    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        //Obtener nuemro de identidad
        String numeroIdentidad = txtNumeroIdentidad.getText();
        
        //Verificar si esta vacio
        if (numeroIdentidad.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese el nuemro de identidad del cliente");
            return;
        }
        
        //Buscar al cliente
        Cliente cliente = banco.buscarCliente(numeroIdentidad);
        
        //Verificar si el cliente existe
        if (cliente == null) {
            JOptionPane.showMessageDialog(this, "Cliente no encontrado. Verifique el numero de identidad");
            return;
        }
        
        //Dar baja
        banco.bajaCliente(numeroIdentidad);
        JOptionPane.showMessageDialog(this, "Cliente dado de baja excitosamente");
        //Cerrar la ventana
        this.dispose();
    }//GEN-LAST:event_btnConfirmarActionPerformed

